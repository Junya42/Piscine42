int		ft_is_prime(int nb)
{
	int i;

	i = 2;
	if (nb < 2 || nb > 2147483647)
		return (0);
	while (i < nb)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}

#include <stdio.h>

int main()
{
	printf("%d\n", ft_is_prime(2147483647));
}
