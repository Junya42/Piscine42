int		ft_sqrt(int nb)
{
	int res;
	int i;

	res = 0;
	i = 1;
	if (nb < 0)
		return (0);
	if (nb == 1)
		return (1);
	while (res < nb && res >= 0)
	{
		res = i * i;
		if (res == nb)
			return (i);
		i++;
	}
	return (0);
}
